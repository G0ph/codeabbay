package main

import (
	"fmt"
	"os"
)

func bicycl() {
	f, _ := os.Open("text.txt")
	var s, w1, w2 float64
	var c int
	fmt.Fscanln(f, &c)
	for i := 0; i < c; i++ {
		fmt.Fscan(f, &s, &w1, &w2)
		w := w1 + w2
		t := s / w
		s1 := w1 * t
		fmt.Printf("%.14g ", s1)
	}
}

